class Camisas:
    def __init__(self,color,tela,talla,precio):
        self.color = color
        self.tela = tela
        self.talla = talla
        self.precio = precio

    def info(self):
        print(f'El color de la camisa es: {self.color}\nLa tela de la camisa es: {self.tela}\nLa talla de la camisa es: {self.talla}\nEl precio de la camisa es: {self.precio}')
        print('-'*30)


