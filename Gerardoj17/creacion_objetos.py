class Libreta:
    def __init__(self,color,tamaño,uso,precio):
        self.color = color
        self.tamaño = tamaño
        self.uso = uso
        self.precio = precio

    def info(self):
        print(f'El color de la libreta es: {self.color}\nEl tamaño de la libreta es: {self.tamaño}\nEl uso que se le da a la libreta es: {self.uso}\nEl precio de la libreta es: {self.precio}')
        print('-'*30)
        


