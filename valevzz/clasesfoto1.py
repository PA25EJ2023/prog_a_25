class Espejo:
    def __init__(self,numero,color,marca,forma,tamaño):
        self.numero = numero
        self.color = color
        self.marca = marca
        self.forma = forma
        self.tamaño = tamaño

    def info (self):
        print(f"Numero espejo: {self.numero} \nColor: {self.color} \nMarca: {self.marca} \nForma: {self.forma} \nTamaño: {self.tamaño}")


