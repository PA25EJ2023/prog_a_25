class Pluma:
    def __init__(self,color,precio,marca,tipo):
        self.color = color
        self.precio = precio
        self.marca = marca
        self.tipo = tipo

    def inform(self):
        print(f'Color:{self.color}\nPrecio:{self.precio}\nMarca:{self.marca}\nTipo:{self.tipo}\n')

